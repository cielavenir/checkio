from collections import defaultdict
def BF(s):
	buffer=defaultdict(int)
	ptr=0
	def execute(s):
		nonlocal buffer,ptr
		ret=''
		i=0
		while i<len(s):
			if s[i]=='>': ptr+=1
			elif s[i]=='<': ptr-=1
			elif s[i]=='+': buffer[ptr]+=1
			elif s[i]=='-': buffer[ptr]-=1
			elif s[i]=='.': ret+=chr(buffer[ptr])
			elif s[i]==',': pass #getchar
			elif s[i]=='[':
				if buffer[ptr]: ret+=execute(s[i+1:])
				marker=1
				while marker:
					if s[i+1]=='[': marker+=1
					if s[i+1]==']': marker-=1
					i+=1
			elif s[i]==']':
				if buffer[ptr]: i=-1
				else: return ret
			i+=1
		return ret
	return execute(s)

